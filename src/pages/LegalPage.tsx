/* ── component ── */

export default function LegalPage() {
  const location = useLocation();
  const [theme, setTheme] = useState<ThemeName>('dark');

  useEffect(() => {
    const saved = window.localStorage.getItem(STORAGE_THEME_KEY);
    if (saved === 'light') setTheme('milky');
  }, []);

  useEffect(() => {
    document.documentElement.dataset.theme = theme === 'milky' ? 'light' : 'dark';
  }, [theme]);

  const t = THEMES[theme];
  const content = LEGAL_CONTENT[location.pathname];

  if (!content) {
    return (
      <div className={cn('flex min-h-screen items-center justify-center', t.bg, t.text)}>
        <p>Страница не найдена.</p>
      </div>
    );
  }

  return (
    <div className={cn('min-h-screen font-sans transition-colors duration-300', t.bg, t.text)}>
      <div className="mx-auto max-w-2xl px-6 py-10 md:py-16">
        {/* nav */}
        <div className="mb-8 flex items-center justify-between">
          <Link
            to="/"
            className={cn('inline-flex items-center gap-2 text-sm transition-colors hover:underline', t.textMuted)}
          >
            <ArrowLeft className="h-4 w-4" />
            На главную
          </Link>
          <button
            type="button"
            onClick={() => setTheme((c) => (c === 'dark' ? 'milky' : 'dark'))}
            className={cn('rounded-full border p-2 transition-colors', t.border, t.textMuted)}
            aria-label="Переключить тему"
          >
            {theme === 'dark' ? <Sun className="h-4 w-4" /> : <Moon className="h-4 w-4" />}
          </button>
        </div>

        {/* title */}
        <h1 className={cn('mb-2 text-2xl font-medium tracking-tight md:text-3xl', t.textStrong)}>
          {content.title}
        </h1>
        <p className={cn('mb-8 text-sm', t.textSubtle)}>
          Последнее обновление: {content.lastUpdated}
        </p>

        <div className={cn('mb-8 h-px', t.divider)} />

        {/* sections */}
        <div className="space-y-8">
          {content.sections.map((section) => (
            <section key={section.heading}>
              <h2 className={cn('mb-3 text-lg font-medium', t.textStrong)}>
                {section.heading}
              </h2>
              <div className="space-y-2">
                {section.body.map((paragraph, i) => (
                  <p key={i} className={cn('text-sm leading-relaxed', t.text)}>
                    {paragraph}
                  </p>
                ))}
              </div>
            </section>
          ))}
        </div>

        {/* footer */}
        <div className={cn('mt-12 h-px', t.divider)} />
        <div className={cn('mt-6 flex flex-wrap items-center gap-x-4 gap-y-2 text-xs', t.textSubtle)}>
          <span>© 2026 WW.pro</span>
          <Link to="/terms" className="hover:underline">Условия</Link>
          <Link to="/privacy" className="hover:underline">Конфиденциальность</Link>
          <Link to="/refund" className="hover:underline">Возврат</Link>
          <Link to="/cookies" className="hover:underline">Cookies</Link>
        </div>
      </div>
    </div>
  );
}
